/*问题描述
　　123321是一个非常特殊的数，它从左边读和从右边读是一样的。
　　输入一个正整数n， 编程求所有这样的五位和六位十进制数，满足各位数字之和等于n 。
输入格式
　　输入一行，包含一个正整数n。
输出格式
　　按从小到大的顺序输出满足条件的整数，每个整数占一行。
样例输入
52
样例输出
899998
989989
998899
数据规模和约定
　　1<=n<=54。*/ 
#include<stdio.h>
int huiwen(int a,int n)
{
	int j=a,s=0,y;
	y=0;
	while(j!=0)//1221
	{
		y=y*10+j%10;
		s=s+j%10;
		j=j/10;
	}
	// y=1 s=1 j=122
	// y=12 s=3 j=12
	// y=122 s=5 j=1
	// y=1221 s=6 j=0
	if(y==a&&s==n)
	  return 1;
	else
	  return 0;
}

int main()
{
	int n,i;
	scanf("%d",&n);
	for(i=10000;i<=999999;i++)
	{
		if(huiwen(i,n))
		printf("%d\n",i);
	}
	return 0;
}
